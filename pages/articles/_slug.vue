<template>
  <v-container>
    <div>
      <v-alert
        v-if="page.wip"
        text
        color="warning"
        dark
        outlined
        rounded="lg"
        icon="mdi-alert">
        本文章尚未完成，内容可能出现较大改动
      </v-alert>
      <v-card v-if="!createTime" class="post-loading-card">
        <div class="post-loading-card-header">
          <v-skeleton-loader type="avatar" />
          <div class="post-loading-card-header-texts">
            <v-skeleton-loader type="heading" />
            <v-skeleton-loader type="text" class="text-loading" />
          </div>
        </div>
        <v-divider />
        <v-card-text>
          <v-skeleton-loader type="text" />
          <v-skeleton-loader type="text" />
          <v-skeleton-loader type="text" max-width="50%" />
          <v-skeleton-loader type="text" />
          <v-skeleton-loader type="text" />
          <v-skeleton-loader type="text" max-width="50%" />
        </v-card-text>
      </v-card>
      <v-card v-else class="post-detail-card">
        <div class="post-detail-card-header">
          <div class="width-10px" />
          <v-btn elevation="0" fab text @click="$router.back()">
            <v-icon>mdi-arrow-left</v-icon>
          </v-btn>
          <div>
            <v-card-title>
              {{ page && page.title }}
            </v-card-title>
            <v-card-subtitle>
              <div class="post-times">
                <div>发布时间：{{ createTime }}</div>
              </div>
            </v-card-subtitle>
          </div>
        </div>
        <v-card-text>
          <nuxt-content :document="page" />
        </v-card-text>
      </v-card>
    </div>
  </v-container>
</template>
<script lang="ts">
import Vue from 'vue'
import { IContentDocument } from '@nuxt/content/types/content'
import moment from 'moment'

export default Vue.extend({
  data () {
    return {
      page: {} as IContentDocument
    }
  },
  computed: {
    createTime (): string {
      if (this.page.createdAt) {
        return moment(this.page.createdAt).format('YYYY 年 MM 月 D 日 HH:mm:ss')
      }
      return ''
    }
  },
  watch: {
    page (val: IContentDocument) {
      if (val.title) {
        document.title = '柠喵的布洛阁 - ' + val.title
      }
    }
  },
  mounted () {
    this.getData()
  },
  methods: {
    async getData () {
      const page = await this.$content('articles', this.$route.params.slug).fetch()
      if (!Array.isArray(page)) {
        setTimeout(() => {
          this.page = page
        }, 500)
      }
    }
  }
})
</script>
